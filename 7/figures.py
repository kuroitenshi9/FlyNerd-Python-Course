#trojkat
def triangle_field(base, height):
   pole_trojkata = base*height/2
   return pole_trojkata


#kolo
def circle_field(r):
   pole_kola = 3.14 * r**2
   return pole_kola


#kwadrat
def square_field(a):
   pole_kwadratu = a**2
   return pole_kwadratu


#trapez
def trapeze_field(a, b, h):
   pole_trapezu =  (a + b)*h/2
   return pole_trapezu


